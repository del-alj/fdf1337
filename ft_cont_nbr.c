#include "ft_fdf.h"

int	ft_cont_nbr(char *str)
{
	int x;
	int i;
	int len;

	i = 0;
	x = 0;
	len = (int)ft_strlen(str);
	while (str && str[i] != '\0' && i < len)
	{
		if (str[i] == ' ' && str[i + 1] != ' ')
			x++;
		i++;
	}
	return (x);
}
