#include "ft_fdf.h"

int	ft_check_nbr(char *str, int i)
{
	if (str[i] == '\0')
		ft_error_function();
	while (str[i] != '\0' && str[i] != ' ')
	{
		if (str[i] == '-')
			i++;
		if ((str[i] >= '0' && str[i] <= '9'))
			return (1);
		i++;
	}
	return (0);
}
